/** Собираем компактный контекст заявки для модели (без «воды»). */
export function applicationContext(app: Application): string {
  const r = app.report;
  const a = app.analysis;
  const lines: string[] = [];
  lines.push(`## Текущая заявка №${app.id} (статус: ${app.status})`);
  lines.push(`Клиент: ${r.borrower_name}${r.birth_date ? `, д.р. ${formatDate(r.birth_date)}` : ""}${r.inn ? `, ИНН ${r.inn}` : ""}.`);
  lines.push(`Отчёт от ${formatDate(r.report_date)}. Кредитный рейтинг: ${r.credit_rating ?? "н/д"}.`);
  lines.push(`Действующих кредитов: ${a.active_contracts}. Общий долг: ${rub(a.total_current_debt)}.`);
  lines.push(
    `Уже выплачено: ${rub(a.paid.total_paid)} (в основной долг ${rub(a.paid.paid_principal)}, в проценты ${rub(a.paid.paid_interest)} — ${Math.round(a.paid.interest_share * 100)}% выплат).`,
  );
  lines.push("### Договоры:");
  for (const c of r.contracts) {
    lines.push(
      `- ${c.creditor}: долг ${rub(c.current_debt)}, ПСК ${c.psk_percent}%, платёж ${rub(c.avg_monthly_payment)}/мес, платежей ${c.payments_made}, выдан ${formatDate(c.origination_date)}.`,
    );
  }
  lines.push("### Сценарии:");
  for (const s of a.scenarios) {
    lines.push(
      `- ${s.title}: платёж ${rub(s.monthly_payment)}/мес, срок ${s.duration_months} мес, итого ${rub(s.total_pay)}, экономия ${rub(s.savings_vs_continue)}.`,
    );
  }
  if (a.compliance.length) {
    lines.push("### Compliance-сигналы: " + a.compliance.map((f) => f.title).join("; ") + ".");
  }
  return lines.join("\n");
}

/** Контекст аналитики воронки — чтобы ассистент отвечал «сколько поступило/закрыто». */
export function analyticsContext(summary: {
  total: number;
  byStatus: Record<string, number>;
  won: number;
  lost: number;
  totalDebtAnalyzed: number;
  potentialSavings: number;
}): string {
  return [
    "## Сводка по всем заявкам (аналитика)",
    `Всего заявок: ${summary.total}. Закрыто успешно: ${summary.won}. Отказов: ${summary.lost}.`,
    `Проанализировано долгов: ${rub(summary.totalDebtAnalyzed)}. Потенциальная экономия клиентам: ${rub(summary.potentialSavings)}.`,
    "По статусам: " + Object.entries(summary.byStatus).map(([k, v]) => `${k}=${v}`).join(", ") + ".",
  ].join("\n");
}
